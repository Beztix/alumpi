				<h1>Absolventenfeier</h1>

				<p>
				Eine Reihe von nervösen Studenten steht am Eingang des Hörsaals. Man hört leise Musik spielen, das Foyer ist festlich dekoriert. 
				Man kann das Tippen von fein herausgeputzten Schuhen hören, hochgesteckte Frisuren im Abendlicht schimmern sehen und erkennen, wie den Krawattenknoten der letzten Schliff gegeben wird. 
				Studenten? Nicht mehr lange. Hier werden heute neue Absolventen geboren! 
				Die letzten Staubkörner werden von den festlichen Kleidern entfernt und schon marschieren sie zum letzten Abenteuer des Studiums.
				</p>
				<p>
				Die Absolventenfeier der Fakultät I ist unterteilt in zwei Akte. 
				Ein offizieller Teil mit Festvortrag, in dem die Absolventen gebührend verabschiedet werden, und ein gemütlicher Teil, nämlich die anschließenden Feierlichkeiten mit Buffet, 
				Cocktails der Physikerbar und gemütlichem Beisammensein im Foyer des NWII mit musischer Untermalung. Während die Verabschiedung und der Festvortrag für jedermann frei zugänglich sind, 
				sind die Absolventen mit ihren Familien, Freunden und Professoren bei den gemütlichen Feierlichkeiten unter sich. Haben Sie ihr Studium erst kürzlich abgeschlossen oder noch nicht die 
				Zeit gefunden Ihren Abschluss ausgiebigst zu feiern? Dann ergreifen Sie hier die Gelegenheit: Bei uns wird jedem Absolvent eine Urkunde der Fakultät verliehen, gleichzeitig werden die 
				Absolventen in einer Präsentation vorgestellt. Das Programm der Feier gibt es ######.
				</p>


				<h2>WICHTIG: Organisationsteam</h2>
				<p>
				Die Absolventenfeiern waren bisher immer von Absolventen mitorganisiert. Der Verein "aluMPI" steuerte dazu die Internetplattform und den groben Ablauf der Feier, 
				sowie wichtige Erfahrungen beim Organisieren der letzten Feiern bei. Der Verein verfügt außerdem über die finanziellen Mittel, die Unkosten der Feier auszulegen. 
				Für die Details sollten allerdings die Absolventen zuständig sein, denn der Vorstand des Vereins ist über die Grenzen Deutschlands hinaus verstreut. 
				Ohne ein Organisationsteam von mindestens sechs Leuten ist es uns also nicht möglich, eine Absolventenfeier auf die Beine zu stellen.
				</p>
				<p>
				Zu organisieren gilt es die Tischdeko, den Festredner, das Buffet, die Band, ein Abschiedsgeschenk, die Einladungen und beispielsweise auch Helfer aus den Reihen der Studenten, 
				damit ihr an der Feier nicht selbst auf- und abbauen, oder Getränke ausschenken müsst. Fast alles ist per Telefon machbar. Bist du also wählerisch, was beispielsweise das Essen betrifft, 
				oder möchtest du, dass die Feier in einem erträglichen finanziellen Rahmen bleibt? Oder bist du Student und möchtest deine Shake-skills ausbauen, oder auch anderweitig mithelfen, 
				so dass deine Mitstudenten eine schöne Feier erleben? Dann hilf mit: alumpi@uni-bayreuth.de
				</p>



