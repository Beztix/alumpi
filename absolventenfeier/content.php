				<h2>Der offizielle Teil</h2>

				<h3>Zuschauer für den offiziellen Teil</h3>
				<p>
				Zuschauer des offiziellen Teils müssen sich nicht anmelden. Jeder kann sich die Verabschiedung der Absolventen und den Festvortrag anhören und ansehen.
				</p>

				<h3>Anmeldung erforderlich bei...</h3>
				<p>
				Eine Anmeldung ist im offiziellen Teil nur dann erforderlich, wenn man als Absolvent an der Zeremonie mit Urkundenverleih teilnehmen möchte.
				</p>

				<h3>Vorraussetzungen für den Urkundenverleih</h3>
				<p>
				Jeder, der zum Zeitpunkt der Feier bereits sein Zeugnis erhalten hat, kann sich auch für die Zeremonie mit Urkundenverleih anmelden. 
				Die einzige Vorraussetzung ist, dass man im Absolventen- und Förderverein Mitglied ist. Keine Sorge, diejenigen, die das nicht bleiben möchten, 
				können sich im Anschluss an die Absolventenfeier ohne Kündigungsfrist wieder austragen. Auch vom Mitgliedsbeitrag ist man im ersten Jahr freigestellt.
				</p>

				<h3>Anmelden ja, aber wo?</h3>
				<p>
				Den Mitgliedern des Vereins steht zur Anmeldung für die Zeremonie mit Urkundenverleih das ##### im Mitgliederbereich zur Verfügung, das auch zur Anmeldung für das Buffet dient.
				</p>

				<h3>Kann man sich als Absolvent zur Zeremonie mit Urkundenverleih anmelden ohne am Buffet teilzunehmen?</h3>
				<p>Nein, das ist derzeit nicht möglich.</p>

				

