					<h2>Neue AluMPI-Homepage</h2>
					
					<p>
					Der Absolventen- und Förderverein MPI Uni Bayreuth e.V. ist stolz, seine neue Homepage präsentieren zu können!<br>
					Nach einigen Wochen Entwicklungsarbeit, in denen die Homepage von Grund auf neu geschrieben wurde, ist diese nun einsatzbereit.
					Die bisherigen Funktionalitäten wie die Anmeldung zum Verein, die Anmeldung zur Absolventenfeier und der interne Mitgliederbereich bleiben trotz grundlegender 
					Neuimplementierung erhalten.
					Die Anmeldung zur Absolventenfeier kann zukünftig sowohl für aktuelle Absolventen und ihre Gäste als auch für separate Gäste und Fakultätsangehörige über die Homepage erfolgen, 
					um auch letzteren einen einfacheren Zugang zur Absolventenfeier zu ermöglichen.<br>
					Das moderne Design der Seite orientiert sich bereits am neuen Design der Websites der Uni Bayreuth, auf das die Lehrstuhlseiten der Universität in Zukunft umgestellt werden.
					Technisch wurde die Homepage mit PHP umgesetzt und wird auf einem eigenen Server gehostet, die Datenbank wird jedoch weiterhin vom IT-Servicezentrum der Uni Bayreuth gestellt.
					Die Homepage wurde bereits einem inoffiziellen Security-Audit unterzogen, um den Datenschutz der Mitgliedsdaten auch zukünftig zu gewährleisten.
					In naher Zukunft wird eine Umstellung der Webseite auf HTTPS erfolgen, es sind außerdem einige neue Funktionalitäten in Planung, die im Laufe des Jahres implementiert werden.<br>
					<br>
					Wir freuen uns darauf, Sie auf unserer neuen Homepage zu begrüßen!<br>
					Bei Anmerkungen, Fragen oder Problemen wenden Sie sich bitte an alumpi@uni-bayreuth.de
					</p>
